    final patterns = _questionPatterns[targetLang] ?? {};
    final sortedEntries = patterns.entries.toList()
      ..sort((a, b) => b.key.length.compareTo(a.key.length)); // ✅ Décroissant

    for (var entry in sortedEntries) {
      if (translated.startsWith(entry.key)) {
        translated = translated.replaceFirst(entry.key, entry.value);
        break;
      }
    }

    // 2. ✅ NOUVEAU : Gérer les contractions françaises AVANT word boundaries
    // Ordre important : expressions longues d'abord !
    translated = _replaceContractions(translated, targetLang);

    // 3. Déterminer le domaine et appliquer les termes
    final domains = _detectDomains(question, category);
    // ✅ AMÉLIORATION : caseSensitive = true pour arabe (diacritiques importantes)
    final useCaseSensitive = (targetLang == 'ar' || targetLang == 'he');

    for (var domain in domains) {
      final terms = _domainTerms[domain]?[targetLang] ?? {};
      for (var entry in terms.entries) {
        // Remplacement avec sensibilité à la casse adaptée
        translated = translated.replaceAll(
            RegExp('\\b${RegExp.escape(entry.key)}\\b',
                caseSensitive: useCaseSensitive), // ✅ true pour AR/HE
            entry.value);
        // Ajouter remplacement sans boundary pour cas apostrophe
        translated = translated.replaceAll(
            RegExp("${RegExp.escape(entry.key)}'",
                caseSensitive: useCaseSensitive),
            "${entry.value}'");
      }
    }

    return translated;
  }

