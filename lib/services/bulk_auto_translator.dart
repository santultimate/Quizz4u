  /// Détecte les domaines dans une question
  static List<String> _detectDomains(String question, String? category) {
    // ✅ TOUJOURS inclure "common" pour les mots fréquents (de, le, la, etc.)
    final domains = <String>['common'];
    final lowerQuestion = question.toLowerCase();
    final lowerCategory = category?.toLowerCase() ?? '';

    // ✅ AMÉLIORATION 1: Catégorie "Culture générale" inclut TOUS les domaines
    if (lowerCategory.contains('culture') &&
        lowerCategory.contains('général')) {
      domains.addAll(
          ['history', 'geography', 'science', 'math', 'sports', 'music']);
      return domains; // Retour anticipé avec tous les domaines
    }

    // Détection par catégorie (méthode existante)
    if (lowerCategory.contains('histoire') ||
        lowerCategory.contains('history')) {
      domains.add('history');
    }
    if (lowerCategory.contains('géographie') ||
        lowerCategory.contains('afrique')) {
      domains.add('geography');
    }
    if (lowerCategory.contains('science') ||
        lowerCategory.contains('santé') ||
        lowerCategory.contains('health')) {
      domains.add('science');
    }
    if (lowerCategory.contains('math')) {
      domains.add('math');
    }
    if (lowerCategory.contains('football') || lowerCategory.contains('sport')) {
      domains.add('sports');
    }
    if (lowerCategory.contains('musique') || lowerCategory.contains('music')) {
      domains.add('music');
    }

    // ✅ AMÉLIORATION 2: Détection par mots-clés dans le CONTENU de la question
    // Historique
    if (lowerQuestion.contains(RegExp(
        r'\b(roi|reine|empereur|empire|guerre|bataille|révolution|président|indépendance|siècle|dynastie)\b'))) {
      if (!domains.contains('history')) domains.add('history');
    }

    // Géographique
    if (lowerQuestion.contains(RegExp(
        r'\b(pays|ville|capitale|région|continent|fleuve|montagne|océan|mer|désert)\b'))) {
      if (!domains.contains('geography')) domains.add('geography');
    }

    // Scientifique
    if (lowerQuestion.contains(RegExp(
        r'\b(planète|étoile|cellule|organe|atome|molécule|énergie|vitesse|température)\b'))) {
      if (!domains.contains('science')) domains.add('science');
    }

    // Mathématique
    if (lowerQuestion.contains(RegExp(
        r'\b(nombre|carré|racine|somme|puissance|triangle|cercle|angle)\b'))) {
      if (!domains.contains('math')) domains.add('math');
    }

    // Détection par mots-clés dans la question (ancien code)
    if (lowerQuestion.contains('pays') ||
        lowerQuestion.contains('capitale') ||
        lowerQuestion.contains('ville')) {
      if (!domains.contains('geography')) domains.add('geography');
    }
    if (lowerQuestion.contains('empire') ||
        lowerQuestion.contains('guerre') ||
        lowerQuestion.contains('roi')) {
      if (!domains.contains('history')) domains.add('history');
    }
    if (lowerQuestion.contains('planète') ||
        lowerQuestion.contains('atome') ||
        lowerQuestion.contains('énergie') ||
        lowerQuestion.contains('virus') ||
        lowerQuestion.contains('organe')) {
      if (!domains.contains('science')) domains.add('science');
    }
    if (lowerQuestion.contains('cube') ||
        lowerQuestion.contains('carré') ||
        lowerQuestion.contains('nombre')) {
      if (!domains.contains('math')) domains.add('math');
    }

    return domains;
  }

  /// Traduit une liste de réponses
  static List<String> translateAnswers(List<String> answers, String targetLang,
      {String? category}) {
    return answers
        .map((answer) =>
            translateQuestion(answer, targetLang, category: category))
        .toList();
  }
