  // Obtenir une traduction
  static String translate(String key) {
    return _translations[_currentLanguage]?[key] ??
        _translations['fr']?[key] ??
        key; // Fallback vers français puis vers la clé
  }

  // Obtenir une traduction avec paramètres
  static String translateWithParams(String key, Map<String, String> params) {
    String translation = translate(key);

    // Remplacer les paramètres dans la traduction
    params.forEach((param, value) {
      translation = translation.replaceAll('{$param}', value);
    });

    return translation;
  }

  // Vérifier si une traduction existe
  static bool hasTranslation(String key) {
    return _translations[_currentLanguage]?.containsKey(key) ?? false;
  }

  // Obtenir toutes les traductions pour la langue actuelle
  static Map<String, String> getAllTranslations() {
    return _translations[_currentLanguage] ?? _translations['fr']!;
  }
