import 'localization_service.dart';

class TranslationService {
  static String _currentLanguage = 'fr';
  static bool _isListening = false;

  // Initialiser le service
  static Future<void> initialize() async {
    _currentLanguage = await LocalizationService.getCurrentLanguage();
    print('[TranslationService] 🌍 Langue initialisée: $_currentLanguage');

    // Écouter les changements de langue une seule fois
    if (!_isListening) {
      _setupLanguageListener();
      _isListening = true;
    }
  }

  // Écouter les changements de langue depuis LocalizationService
  static void _setupLanguageListener() {
    LocalizationService.languageChangeStream.listen((language) {
      _currentLanguage = language;
      print(
          '[TranslationService] 🔄 Langue mise à jour automatiquement: $language');
    });
  }

  // Changer la langue
  static Future<void> changeLanguage(String language) async {
    if (LocalizationService.isLanguageSupported(language)) {
      await LocalizationService.setLanguage(language);
      _currentLanguage = language;
      print('[TranslationService] 🌍 Langue changée vers: $language');
    }
  }

  // Obtenir la langue actuelle
  static String getCurrentLanguage() => _currentLanguage;

